document.addEventListener("DOMContentLoaded", function () {
    fetch("headerData.json")
        .then(response => response.json())
        .then(data => {
            // Populate Top Links
            let topLinks = document.getElementById("top-links");
            topLinks.innerHTML = data.topLinks.map(link => `<li><a href="${link.url}">${link.title}</a></li>`).join('');

            // Populate Contact Info
            document.getElementById("phone").textContent = data.contactInfo.phone;
            document.getElementById("email").textContent = data.contactInfo.email;
            document.getElementById("email").href = `mailto:${data.contactInfo.email}`;

            // Set Logo
            document.getElementById("logo").src = data.logo;

            // Populate Navigation Menu
            let navMenu = document.getElementById("nav-menu");
            navMenu.innerHTML = data.menu.map(item => `
            <li>
                <a href="${item.url}">${item.title} ${item.dropdown ? '<i class="icofont-rounded-down"></i>' : ''}</a>
                ${item.dropdown ? `<ul class="dropdown">${item.dropdown.map(sub => `<li><a href="${sub.url}">${sub.title}</a></li>`).join('')}</ul>` : ''}
            </li>
        `).join('');

            // Set Appointment Button
            let appointmentBtn = document.getElementById("appointment-btn");
            appointmentBtn.textContent = data.appointment.text;
            appointmentBtn.href = data.appointment.url;
        })
        .catch(error => console.error("Error fetching header data:", error));
});

